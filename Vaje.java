public class Vaje {

    public static void main(String[] args) {
        System.out.println("Hello world!");
        System.out.println(vsota(21, 21));
        System.out.println(deli(42, 3));
    }

    public static int vsota(int a, int b) {
        return a + b;
    }

    public static int deli(int a, int b) {
        return a / b;
    }
}